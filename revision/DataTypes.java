public class DataTypes {
  public static void main(String[] args) {

    // datatypes
    // java 8  primitive datatypes
    // byte // 1 byte // 8 bit

    byte b = 127;

    // short // 2 byte // 16 bit

    short s = 1212;

    // int // 4 byte // 32 bit
    int num = 1234567890;

    // long // 8 byte // 64 bit
    long l = 12345678901234L;

    // float // 4 byte // 32 bit
    float f = 123.456f;

    // double // 8 byte // 64 bit
    double d = 123.4567890123456789;

    // char // 2 byte // 16 bit
    char ch = 'A';

    // char ch = '1';

    // boolean // 1 bit
    boolean b1 = false;



  }
}
