public class Datatypes {
  public static void main(String[] args) {

    // datatypes

    // what type value that hold
    // what is the size of the value

    // primitive // 8 primitive data types
    // byte

    byte a = 127; // 1 byte  // 8 bit

    // short
    short b = 32767; // 2 bytes // 16 bit

    // int
    int c = 2147483647; // 4 bytes // 32 bit

    // long
    long d = 9223372036854775807l; // 8 bytes // 64 bit

    // float
    float e = 3.1454545455f; // 4 bytes // 32 bit

    // double
    double f = 3.144757984598754449759875; // 8 bytes // 64 bit

    // character
    char g = ' '; // 2 bytes // 16 bit

    // boolean
    boolean h = true; // 1 bit


    System.out.println(a);
    System.out.println(b);
    System.out.println(c);
    System.out.println(d);
    System.out.println(e);
    System.out.println(f);
    System.out.println(g);
    System.out.println(h);

  }
}
